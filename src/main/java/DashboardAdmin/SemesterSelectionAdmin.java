    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        String selectedSemester = (String) jComboBox1.getSelectedItem();

        try {

            if ("Show All".equalsIgnoreCase(selectedSemester)) {
                String query = "SELECT SemesterID, SemesterName, Semester FROM semester WHERE `TeacherID`=?";
                pst = conn.prepareStatement(query);
                pst.setInt(1, SessionTeacher.loggedInTeacherId);
            } else {
                String query1 = "SELECT SemesterID, SemesterName, Semester FROM semester WHERE TeacherID=? AND Semester=?";
                pst = conn.prepareStatement(query1);
                pst.setInt(1, SessionTeacher.loggedInTeacherId);
                pst.setString(2, selectedSemester);
            }

            rs = pst.executeQuery();

            DefaultTableModel model = new DefaultTableModel() {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false; // Makes all cells non-editable
                }
            };

            model.addColumn("Semester ID");
            model.addColumn("Semester Name");
            model.addColumn("SemesterStatus");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("SemesterID"),
                    rs.getString("SemesterName"),
                    rs.getString("Semester")
                });
            }

            jTable1.setModel(model);
            jTable1.getColumnModel().getColumn(0).setMinWidth(0);
            jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
            jTable1.getColumnModel().getColumn(0).setWidth(0);

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error fetching semester data: " + ex.getMessage());
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

