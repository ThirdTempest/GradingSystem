    private void table() {
        try {
            String sql = "SELECT * FROM `semester` WHERE `TeacherID`=?";
            pst = conn.prepareStatement(sql);
            pst.setInt(1, SessionTeacher.loggedInTeacherId);
            rs = pst.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // Clear existing rows
            model.setColumnCount(0); // Clear existing columns (optional but safe)

            model.addColumn("Semester ID");
            model.addColumn("Semester Name");
            model.addColumn("Semester"); // Only if this is a valid column

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("SemesterID"),
                    rs.getString("SemesterName"),
                    rs.getString("Semester") // Remove if not in your table
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(rootPane, "Error loading semesters: " + e.getMessage());
        }
    }

    public int getSemesterID(int SemesterID) { // get SectionID for SessionSection in SessionID. (SectionID) becomes static
        String sql = "SELECT * FROM `section` WHERE `SectionID` = ?";

        try {
            pst = conn.prepareStatement(sql);
            pst.setInt(1, SemesterID);
            rs = pst.executeQuery();

            if (rs.next()) {
                SemesterID = rs.getInt("SemesterID");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error retrieving SemesterID: " + e.getMessage());
        }

        return SemesterID;
    }

