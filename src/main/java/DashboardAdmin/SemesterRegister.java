    private void jaddsemesterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jaddsemesterActionPerformed
        String semester = jsemester.getText();//adding new Section
        String selectedsemester = (String) jComboBox1.getSelectedItem();

        boolean isValid = emptysemester(semester, jComboBox1, errorsemester, errorsection);//check if its empty

        if (!isValid) {
            return;//return if empty
        }

        try {
            st = conn.createStatement();

            st.executeUpdate(" INSERT INTO `semester`(`TeacherID`, `SemesterName`, `Semester`)"
                    + "VALUES ('" + SessionTeacher.loggedInTeacherId + "','" + semester + "','" + selectedsemester + "')");

            JOptionPane.showMessageDialog(rootPane, "Semester Created!");

            this.setVisible(false);
            new SemesterSelectionAdmin(this.id, this.first, this.middle, this.last).setVisible(true);

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jaddsemesterActionPerformed

