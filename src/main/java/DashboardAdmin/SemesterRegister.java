    @Override
    public boolean emptysemester(String semester, JComboBox<String> comboBox, JLabel errorsemester, JLabel errorsection) { // implements method from SectionValidation
        boolean isValid = true;
        if (semester.isEmpty()) {
            setError(errorsection, "Semester name cannot be empty.");
            isValid = false;
        } else {
            clearError(errorsection);
        }
        if (comboBox.getSelectedItem() == null) {
            setError(errorsemester, "Please select semester");
            isValid = false;
        } else {
            clearError(errorsemester);
        }
        return isValid;
    }

    @Override
    public void setError(JLabel label, String message) {
        label.setText(message);
        label.setForeground(java.awt.Color.RED);
    }

    @Override
    public void clearError(JLabel label) {
        label.setText("");
    }
