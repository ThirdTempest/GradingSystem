    @Override
    public boolean emptySection(String section, JLabel errorsection) { // implements method from SectionValidation
        boolean isValid = true;
        if (section.isEmpty()) {
            setError(errorsection, "Section name cannot be empty.");
            isValid = false;
        } else {
            clearError(errorsection);
        }
        return isValid;
    }

    @Override
    public void setError(JLabel label, String message) {
        label.setText(message);
        label.setForeground(java.awt.Color.RED);
    }

    @Override
    public void clearError(JLabel label) {
        label.setText("");
    }
