    private void jaddsectionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jaddsectionActionPerformed
        String section = jsection.getText(); // Adding new Section
        boolean isValid = emptySection(section, errorsection); // Check if it's empty

        if (!isValid) {
            return; // Return if empty
        }

        try {
            st = conn.createStatement();

            st.executeUpdate("INSERT INTO `section`(`SemesterID`,`SectionName`, `TeacherID`)"
                    + "VALUES ('" + SessionSemester.SemesterID + "','" + section + "','" + SessionTeacher.loggedInTeacherId + "')");

            JOptionPane.showMessageDialog(rootPane, "Section Created!");

            this.setVisible(false);
            new SectionAdmin(this.id, this.first, this.middle, this.last).setVisible(true);

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }//GEN-LAST:event_jaddsectionActionPerformed



